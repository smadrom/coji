describe('S3StorageProvider.exists', () => {
  it('returns true when HeadObjectCommand succeeds', async () => {
    const provider = makeProvider(async () => ({}));
    const result = await provider.exists('frames/abc.jpg');
    expect(result).toBe(true);
  });

  it('returns false on 404 status code', async () => {
    const provider = makeProvider(async () => {
      const err = Object.assign(new Error('Not Found'), {
        $metadata: { httpStatusCode: 404 },
      });
      throw err;
    });
    const result = await provider.exists('missing.jpg');
    expect(result).toBe(false);
  });

  it('returns false on NotFound error name', async () => {
    const provider = makeProvider(async () => {
      const err = Object.assign(new Error('Not Found'), { name: 'NotFound' });
      throw err;
    });
    const result = await provider.exists('missing.jpg');
    expect(result).toBe(false);
  });

  it('returns false on NoSuchKey error name', async () => {
    const provider = makeProvider(async () => {
      const err = Object.assign(new Error('NoSuchKey'), { name: 'NoSuchKey' });
      throw err;
    });
    const result = await provider.exists('missing.jpg');
    expect(result).toBe(false);
  });

  it('rethrows non-404 errors', async () => {
    const provider = makeProvider(async () => {
      const err = Object.assign(new Error('Access Denied'), {
        name: 'AccessDenied',
        $metadata: { httpStatusCode: 403 },
      });
      throw err;
    });
    await expect(provider.exists('secret.jpg')).rejects.toThrow('Access Denied');
  });
});

// ---------------------------------------------------------------------------
// put → getBytes round-trip (integration-style, still using mock send)
// ---------------------------------------------------------------------------

describe('S3StorageProvider put/getBytes round-trip', () => {
  it('stores bytes and retrieves them via independent mock calls', async () => {
    // We can't do a real round-trip without a live S3, but we can verify
    // both commands are called with matching key/bucket and the same bytes.
    const storedBytes = new Uint8Array([99, 88, 77, 66]);
    let putInput: Record<string, unknown> = {};
    let getInput: Record<string, unknown> = {};

    const client = new S3Client({
      region: 'us-east-1',
      credentials: { accessKeyId: 'x', secretAccessKey: 'x' },
    });
    let callCount = 0;
    client.send = mock(async (cmd: unknown) => {
      callCount++;
      const input = (cmd as { input: Record<string, unknown> }).input;
      if (callCount === 1) {
        putInput = input;
        return {}; // PutObject response
      }
      getInput = input;
      return { Body: { transformToByteArray: async () => storedBytes } };
    }) as unknown as typeof client.send;

    const provider = new S3StorageProvider({
      bucket: 'round-trip-bucket',
      region: 'us-east-1',
      accessKeyId: 'x',
      secretAccessKey: 'x',
      client,
    });

    const key = 'clips/video-001.mp4';
    await provider.put(key, storedBytes, 'video/mp4');
    const retrieved = await provider.getBytes(key);

    expect(putInput.Key).toBe(key);
    expect(putInput.Bucket).toBe('round-trip-bucket');
    expect(getInput.Key).toBe(key);
    expect(getInput.Bucket).toBe('round-trip-bucket');
    expect(retrieved).toEqual(storedBytes);
  });
});
